def admin_only(func):
    @wraps(func)
    def decorator(*args, **kwargs):
        if current_user.is_authenticated:
            if current_user.id == 1:
                return func(*args, **kwargs)
            else:
                return 'Admin Only'
        else:
           return func(*args, **kwargs)
    return decorator

def login_required(func):
    @wraps(func)
    def wrapper(*args, **kwargs):
        if current_user.is_authenticated:
            return func(*args, **kwargs)
        else:
            return '<h1>Please Register</h1>'
    return wrapper


